void mn34220_init(VI_PIPE ViPipe)
{
    WDR_MODE_E       enWDRMode;
    HI_U8            u8ImgMode;
    //HI_BOOL          bInit;

    //bInit       = g_astMn34220[ViPipe].bInit;
    enWDRMode   = g_astMn34220[ViPipe]->enWDRMode;
    u8ImgMode   = g_astMn34220[ViPipe]->u8ImgMode;

    if (mn34220_i2c_init(ViPipe))
    {
        return;
    }

    /* When mn34220 first init, config all registers */
    if (WDR_MODE_2To1_LINE == enWDRMode)
    {
        if (2 == u8ImgMode)    /* SENSOR_1080P_30FPS_MODE */
        {
            mn34220_wdr_1080P_2to1_init(ViPipe);
        }
        else if (5 == u8ImgMode)    /* SENSOR_720P_30FPS_MODE */
        {
            mn34220_wdr_720p_2to1_init(ViPipe);
        }
        else
        {
        }
    }
    else if (WDR_MODE_3To1_LINE == enWDRMode)
    {
        if (2 == u8ImgMode)    /* SENSOR_1080P_30FPS_MODE */
        {
            mn34220_wdr_1080p_3to1_init(ViPipe);
        }
        else if (5 == u8ImgMode)    /* SENSOR_720P_30FPS_MODE */
        {
            mn34220_wdr_720p_3to1_init(ViPipe);
        }
        else
        {
        }
    }
    else if (WDR_MODE_4To1_LINE == enWDRMode)
    {
        if (2 == u8ImgMode)    /* SENSOR_1080P_30FPS_MODE */
        {
            mn34220_wdr_1080p_4to1_init(ViPipe);
        }
        else if (5 == u8ImgMode)    /* SENSOR_720P_30FPS_MODE */
        {
            mn34220_wdr_720p_4to1_init(ViPipe);
        }
        else
        {
        }
    }
    else
    {
        if (1 == u8ImgMode)    /* SENSOR_1080P_60FPS_MODE */
        {
            mn34220_linear_1080p60_init(ViPipe);
        }
        else if (2 == u8ImgMode)   /* SENSOR_1080P_30FPS_MODE */
        {
            mn34220_linear_1080p30_init(ViPipe);
        }
        else if (3 == u8ImgMode)
        {
            mn34220_linear_720p120_init(ViPipe);
        }
        else if (4 == u8ImgMode)
        {
            mn34220_linear_VGA240_init(ViPipe);
        }
        else if (5 == u8ImgMode)
        {
            mn34220_linear_720p30_init(ViPipe);
        }
        else
        {
        }
    }

    g_astMn34220[ViPipe]->bInit = HI_TRUE;

    return ;
}

void mn34220_exit(VI_PIPE ViPipe)
{
    mn34220_i2c_exit(ViPipe);

    return;
}

